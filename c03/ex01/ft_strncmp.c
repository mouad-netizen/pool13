int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && n)
	{
		if (s1[i] == s2[i])
		{
			i++;
			n--;
		}
		else
			break ;
	}
	if (n == 0)
		return (0);
	else
		return (s1[i] - s2[i]);
}
